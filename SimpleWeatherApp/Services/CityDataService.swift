    // MARK: - Persistence

    private static let storageKey = "saved_cities"

    static func loadCities() -> [CityWeather] {
        guard let data = UserDefaults.standard.data(forKey: storageKey),
              let cities = try? JSONDecoder().decode([CityWeather].self, from: data) else {
            return defaultCities()
        }
        return cities
    }

    static func saveCities(_ cities: [CityWeather]) {
        if let data = try? JSONEncoder().encode(cities) {
            UserDefaults.standard.set(data, forKey: storageKey)
        }
    }

    // MARK: - Global City Search (Open-Meteo Geocoding API)

    static func searchGlobal(query: String) async -> [GeocodingResult] {
        let trimmed = query.trimmingCharacters(in: .whitespaces)
        guard !trimmed.isEmpty else { return [] }
        var components = URLComponents(string: "https://geocoding-api.open-meteo.com/v1/search")!
        components.queryItems = [
            .init(name: "name", value: trimmed),
            .init(name: "count", value: "15"),
            .init(name: "language", value: "zh"),
            .init(name: "format", value: "json"),
        ]
        guard let url = components.url else { return [] }
        do {
            let (data, _) = try await URLSession.shared.data(from: url)
            let response = try JSONDecoder().decode(GeocodingResponse.self, from: data)
            return response.results ?? []
        } catch {
            return []
        }
    }

